    private void simpanHadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanHadirActionPerformed
        // TODO add your handling code here:
        try {
            // 1. Ambil data dari input
            int idPegawai = Integer.parseInt(idHadir.getText());
            String namaPegawai = namaHadir.getText();
            int tahun = Integer.parseInt(inpuTahun.getText()); 
            int bulan = Integer.parseInt(inputBulan.getText());
            int tanggal = Integer.parseInt(inputTanggal.getText()); 
            int jamMasuk = Integer.parseInt(inputJamMasuk.getText());
            int menitMasuk = Integer.parseInt(inputMenitMasuk.getText());
            int jamKeluar = Integer.parseInt(inputJamKeluar.getText());
            int menitKeluar = Integer.parseInt(inputMenitKeluar.getText());
            
            kehadiranPegawai.entities.Pegawai pegawai = pegawaiDAO.getPegawaiById(idPegawai);
            if (pegawai == null) {
                throw new IllegalArgumentException("ID Pegawai tidak ditemukan.");
            }

            // 2. Validasi input jam dan menit 
            if (jamMasuk < 0 || jamMasuk > 23 || menitMasuk < 0 || menitMasuk > 59 ||
                jamKeluar < 0 || jamKeluar > 23 || menitKeluar < 0 || menitKeluar > 59) {
                throw new IllegalArgumentException("Format jam atau menit tidak valid.");
            }

            // 3. Validasi input tanggal (opsional, tetapi disarankan)
            if (tahun < 1900 || tahun > 2100 || bulan < 1 || bulan > 12 || tanggal < 1 || tanggal > 31) {
                throw new IllegalArgumentException("Format tanggal tidak valid.");
            }

            // 4. Buat objek Date
            Calendar cal = Calendar.getInstance();
            cal.set(Calendar.YEAR, tahun);
            cal.set(Calendar.MONTH, bulan - 1); // Bulan di Calendar dimulai dari 0
            cal.set(Calendar.DAY_OF_MONTH, tanggal);
            Date tanggalDate = cal.getTime();

            // 5. Konversi ke java.sql.Date
            java.sql.Date sqlTanggal = new java.sql.Date(tanggalDate.getTime());

            // 6. Buat objek Time untuk jam masuk dan jam keluar
            Time jamMasukTime = new Time(jamMasuk, menitMasuk, 0);
            Time jamKeluarTime = new Time(jamKeluar, menitKeluar, 0);

            // 7. Hitung durasi kerja (dalam jam)
            long durasiMillis = jamKeluarTime.getTime() - jamMasukTime.getTime();
            double durasiJam = durasiMillis / (1000.0 * 60 * 60);

            // 8. objek Kehadiran
            Kehadiran kehadiran = new Kehadiran(0, idPegawai, sqlTanggal, jamMasukTime, jamKeluarTime, durasiJam);

            // 9. Simpan data ke database
            kehadiranDAO.catatKehadiran(kehadiran);
            JOptionPane.showMessageDialog(this, "Data kehadiran berhasil disimpan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            // 10. Refresh tabel dan bersihkan input
            tampilkanDataKehadiran();
            idHadir.setText("");
            namaHadir.setText("");
            inpuTahun.setText(""); 
            inputBulan.setText(""); 
            inputTanggal.setText(""); 
            inputJamMasuk.setText("");
            inputMenitMasuk.setText("");
            inputJamKeluar.setText("");
            inputMenitKeluar.setText("");

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID Pegawai, jam, dan menit harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data kehadiran: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    
 
    }//GEN-LAST:event_simpanHadirActionPerformed

