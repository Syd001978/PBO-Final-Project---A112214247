    private void tampilkanDataKehadiran() {
     modelTabel.setRowCount(0); // Bersihkan tabel

     List<Kehadiran> daftarKehadiran = kehadiranDAO.getAllKehadiran();
     for (Kehadiran kehadiran : daftarKehadiran) {
         
         long timestamp = kehadiran.getTanggal().getTime(); 
         Date tanggalUtil = new Date(timestamp);
         
         Object[] rowData = {
             kehadiran.getIdPegawai(),
             pegawaiDAO.getPegawaiById(kehadiran.getIdPegawai()).getNama(), // Ambil nama dari database
             dateFormat.format(tanggalUtil),
             timeFormat.format(kehadiran.getJamMasuk()),
             timeFormat.format(kehadiran.getJamKeluar()),
             String.format("%.0f jam", kehadiran.getDurasiKerja()) // Format durasi kerja (contoh: 7 jam)
         };
         modelTabel.addRow(rowData);
     }
 }

