    private void tampilkanDataAbsen() {
        modelTabel.setRowCount(0); // Bersihkan tabel

        List<Absensi> daftarAbsen = absensiDAO.getAllAbsensi();
        for (Absensi absensi : daftarAbsen) {
            Pegawai pegawai = pegawaiDAO.getPegawaiById(absensi.getIdPegawai());
            Object[] rowData = {
                absensi.getIdPegawai(),
                pegawai.getNama(),
                dateFormat.format(absensi.getTanggal()),
                absensi.getAlasan()
            };
            modelTabel.addRow(rowData);
        }
    }

