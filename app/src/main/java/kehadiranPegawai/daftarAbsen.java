    private void simpanAbsenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanAbsenActionPerformed
        // TODO add your handling code here:
        try {
            // 1. Ambil data dari input
            int idPegawai = Integer.parseInt(idAbsen.getText());
            String namaPegawai = namaAbsen.getText(); 
            int tahun = Integer.parseInt(inputTahunAbsen.getText());
            int bulan = Integer.parseInt(inputBulanAbsen.getText());
            int tanggal = Integer.parseInt(inputTanggalAbsen.getText());
            String alasan = alasanComboBox.getSelectedItem().toString();

            // 2. Validasi ID Pegawai
            Pegawai pegawai = pegawaiDAO.getPegawaiById(idPegawai);
            if (pegawai == null) {
                throw new IllegalArgumentException("ID Pegawai tidak ditemukan.");
            }

            // 3. Validasi input tanggal (opsional)
            if (tahun < 1900 || tahun > 2100 || bulan < 1 || bulan > 12 || tanggal < 1 || tanggal > 31) {
                throw new IllegalArgumentException("Format tanggal tidak valid.");
            }

            // 4. Buat objek Date
            Calendar cal = Calendar.getInstance();
            cal.set(Calendar.YEAR, tahun);
            cal.set(Calendar.MONTH, bulan - 1); // Bulan di Calendar dimulai dari 0
            cal.set(Calendar.DAY_OF_MONTH, tanggal);
            Date tanggalDate = cal.getTime();

            // 5. Konversi ke java.sql.Date
            java.sql.Date sqlTanggal = new java.sql.Date(tanggalDate.getTime());

            // 6. Buat objek Absensi (tanpa jenisAbsensi)
            Absensi absensi = new Absensi(0, idPegawai, sqlTanggal, alasan);

            // 7. Simpan data ke database
            absensiDAO.tambahAbsensi(absensi);
            JOptionPane.showMessageDialog(this, "Data absensi berhasil disimpan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            // 8. Refresh tabel dan bersihkan input
            tampilkanDataAbsen();
            idAbsen.setText("");
            namaAbsen.setText("");
            inputTahunAbsen.setText("");
            inputBulanAbsen.setText("");
            inputTanggalAbsen.setText("");
            alasanComboBox.setSelectedIndex(0); 

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID Pegawai, tahun, bulan, dan tanggal harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data absensi: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_simpanAbsenActionPerformed

